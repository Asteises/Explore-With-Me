CREATE TABLE IF NOT EXISTS endpoint_hit
(

    id      BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    app     VARCHAR,
    uri     VARCHAR,
    ip      VARCHAR,
    created TIMESTAMP
);

CREATE TABLE IF NOT EXISTS view_stats
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    app  VARCHAR,
    uri  VARCHAR,
    hits BIGINT
);